public class Main {
    public static void main(String[] args) {
        // Déclaration des variables
        char caractere = 'A';
        String chaine = "Bonjour";
        int entier = 42;
        long grandEntier = 123456789L;
        float nombreFlottant = 3.14f;
        boolean valeurBooleenne = true;
        
        // Affichage des valeurs stockées
        System.out.println("Valeur de caractere: " + caractere);
        System.out.println("Valeur de chaine: " + chaine);
        System.out.println("Valeur de entier: " + entier);
        System.out.println("Valeur de grandEntier: " + grandEntier);
        System.out.println("Valeur de nombreFlottant: " + nombreFlottant);
        System.out.println("Valeur de valeurBooleenne: " + valeurBooleenne);

        // Stockage de 3.817 dans un int
        int TOTO = (int) 3.817;
        System.out.println("Valeur de TOTO après stockage de 3.817: " + TOTO);
    }
}
